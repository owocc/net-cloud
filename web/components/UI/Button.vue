<template>
	<button :class="buttonThemeClasses" v-bind="$attrs">
		<div class="mr-2">
			<slot name='before'></slot>
		</div>
		{{ props.text }}
	</button>
</template>

<script setup lang="ts">
import clsx from 'clsx'
interface ButtonProps {
	text: string
	rounded: boolean
	primary: boolean
	base: boolean
	center: boolean
	liquid: boolean
}
const props = defineProps<Partial<ButtonProps>>()
const buttonThemeClasses = computed(() =>
	clsx(
		props.rounded ? "rounded-full" : 'rounded-lg',
		props.primary && "text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 font-medium  text-sm px-5 py-2.5 text-center dark:bg-blue-600 dark:hover:bg-blue-700 dark:focus:ring-blue-800",
		props.base && "py-2.5 px-5 text-sm font-medium text-gray-900 focus:outline-none bg-white border border-gray-200 hover:bg-gray-100 hover:text-blue-700 focus:z-10 focus:ring-4 focus:ring-gray-200 dark:focus:ring-gray-700 dark:bg-gray-800 dark:text-gray-400 dark:border-gray-600 dark:hover:text-white dark:hover:bg-gray-700",
		props.center && "flex items-center justify-center",
		props.liquid ? 'w-full' : 'w-auto'

	)
)
</script>